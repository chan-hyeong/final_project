package kosta.finalproject.customer.controller;

import javax.servlet.http.HttpSession;

import org.apache.ibatis.session.SqlSession;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.RequestMapping;

import kosta.finalproject.customer.dao.Topmenu5DAO;

@Controller
public class CustomerController {
	@Autowired
	private SqlSession sqlsession;

	@RequestMapping("/index.do")
	public String index() {
		return "customer/index";
	}

	@RequestMapping("/favorite.do")
	public String favorite(HttpSession session) {
		if (session.getAttribute("id") == null) {
			return "redirect:loginform.do";
		}
		return "customer/favorite";
	}

	@RequestMapping("/main.do") //// 수정된 메인 매핑 6/11 Pm 7:33 ///////////
	public String main(Model model) {

		Topmenu5DAO dao = sqlsession.getMapper(Topmenu5DAO.class);
		model.addAttribute("top5", dao.top5list());
		return "customer/main";
	}

	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	////////////////// 분리해야함
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////// ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

}// end class